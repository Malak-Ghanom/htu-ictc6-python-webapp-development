# Special printing

# declare variables
tabby_cat = "\tI'm tabbed in."          # string with (t)ab
persian_cat = "I'm split\non a line."   # string with (n)ew line
backslash_cat = "I'm \\ a \\ cat."      # string with backslash

fat_cat = """
I'll do a list:
\t* Cat food
\t* Fishes
\t* Catnips\n\t* Grass
"""

print("Now let us print our cats!")

print(tabby_cat)
print(persian_cat)
print(backslash_cat)
print(fat_cat)
